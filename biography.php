			<p>Judge Mark Rusch was born the fourth of five children in Indianapolis, Indiana. His father’s career took the family to New Jersey and then Houston, where Mark graduated from Stratford High School. While attending the University of Dallas, Mark met his future wife Ellen on Groundhog Day in 1976. They married shortly after graduation in 1979 and moved to Houston, where Mark began his legal studies at the University of Houston College of Law. Mark worked full time at an independent oil company while attending law school at night.  He graduated in December of 1983, took the February 1984 Bar Exam and was admitted to the practice of law in May of 1984. After passing the bar, Mark and Ellen moved their family to Plano, where they raised three children and are now enjoying their two grandchildren.</p>

