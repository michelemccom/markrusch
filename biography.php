			<p>Mark entered a crowded race for judge of the newly created County Court at Law #4 bench and won the election in 1996. He presided over misdemeanor criminal cases and civil and mental health matters. </p>

			<p>He was appointed by Governor George W. Bush to the newly created 401st District Court and took that office on September 1, 2000. He was re-elected in 2004, 2008 and 2012. </p>

